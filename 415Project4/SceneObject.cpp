SceneObject::SceneObject(float length, float width, float depth, GLuint vertposition_loc, GLuint vertcolor_loc)
{

	float x1 = 0, x2, y1, y2, z1, z2;
	
	this->length = length;
	this->width = width;
	this->depth = depth;

	x2 = 1.0f;

	y1 = 1.0f / 2;
	y2 = y1 - 1.0f;

	z1 = 1.0f / 2;
	z2 = z1 - 1.0f;
	

	gmtl::identity(this->matrix);

	this->scale = gmtl::makeScale<gmtl::Matrix44f>(gmtl::Vec3f(this->length, this->width, this->depth));
	this->scale.setState(gmtl::Matrix44f::AFFINE);

	#pragma region Data Vectors
	std::vector<GLfloat> vbuffer_data =
	{
		x1, y1, z1, //0
		x1, y2, z1, //1
		x1, y1, z2, //2
		x1, y2, z2, //3
		x2, y1, z1, //4
		x2, y2, z1, //5
		x2, y1, z2, //6
		x2, y2, z2 //7
	};

	std::vector<GLfloat> cbuffer_data =
	{
		0.6f, 0.7f, 0.9f,
		1.0f, 0.8f, 0.9f,
		0.6f, 0.2f, 0.7f,
		0.9f, 0.9f, 0.7f,
		0.2f, 0.5f, 0.8f,
		1.0f, 0.8f, 0.9f,
		0.1f, 0.0f, 0.8f,
		0.9f, 0.6f, 0.1f
	};

	std::vector<GLushort> index_buffer_data =
	{
		0, 4, 1, 5, 3, 7, 2, 6, 0, 4,
		0xFFFF,
		1, 0, 3, 2,
		0xFFFF,
		5, 4, 7, 6,
	};

	#pragma endregion

	this->VAO = VertexArrayObject(vbuffer_data, cbuffer_data, index_buffer_data, vertposition_loc, vertcolor_loc);

}

